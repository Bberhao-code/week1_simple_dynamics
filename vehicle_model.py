"""
vehicle_model.py

存放车辆参数和初始状态。
后面其他脚本（比如仿真、求解 ODE）会从这里导入这些量。
"""

# ======================
# 1. 车辆参数（推荐值）
# ======================

m = 1500.0     # 车辆质量 kg
Iz = 2250.0    # 绕质心的偏航转动惯量 kg*m^2
a = 1.2        # 质心到前轴距离 m
b = 1.6        # 质心到后轴距离 m
Cf = 19000.0   # 前轴侧偏刚度 N/rad
Cr = 33000.0   # 后轴侧偏刚度 N/rad
v = 15.0       # 行驶速度 m/s（约 54 km/h）

# ======================
# 2. 状态变量初值
# ======================

# 状态：侧偏角 beta、横摆角速度 r、位置 x,y、航向角 psi
beta_0 = 0.0   # 初始侧偏角 rad
r_0 = 0.0      # 初始横摆角速度 rad/s
x_0 = 0.0      # 初始位置 x m
y_0 = 0.0      # 初始位置 y m
psi_0 = 0.0    # 初始航向角 rad


# ======================
# 3. 打包成函数（方便后面导入）
# ======================

def vehicle_params():
    """返回车辆参数（可以在别的文件中 from vehicle_model import vehicle_params 使用）"""
    return m, Iz, a, b, Cf, Cr, v


def initial_state():
    """返回状态的初始值"""
    return beta_0, r_0, x_0, y_0, psi_0
